            <h3>History</h3>
            <br />
            <p>
              Barangay Pitogo derived its name from a palm plant called "Pitogo"
              (seen in front of the the Barangay Hall) which abound in the area
              before World War II. Bounded on the North by Guadalupe Nuevo, on
              the West by Pinagkaisahan, on the South by South Cembo. It has an
              area of 19.55 hectares, with a population of 14, 395 (based on
              2015) PSA census.
            </p>
            <br />
            <p>
              The Barangay come into being in 1956 when the Commanding General
              of the Philippine Army saw the need to provide housing area at
              periphery of Fort Bonifacio for Military personnel thus Pitogo was
              one of the Enlisted men's Barrio to be created. From then on the
              influx of AFP families continued to settle one after the other
              until the whole place became full of houses. The affairs of the
              Barangay were then being administratively controlled by the Post
              Commander of Fort Bonifacio. For the first time, election of the
              barangay officials was held on May 17, 1982.
            </p>
            <br />
            <p>
              Through the sincere desire and vigorous support of Mayor Jejomar
              C. Binay, surveying of lots were managed free of charge for the
              residents of Baragay Pitogo. As of today 80% of all lots have
              original certificate of title. New modern high school, elementary
              school, kindergarten, nursery school building covered court and
              ope basketball court were upgraded. Improvement of side street
              drainages are also being undertaken by the Makati City
              Administration. With the Public Safety Department (PSD) and Bantay
              Bayan on 24 hours' duty, peace and order adequately maintained
              City program is being implemented, peace and order situation is
              strictky being monitored. Health and Nutrition services were also
              made, available by the City of Makati and the Barangay Council of
              Pitogo.
            </p>
            <br />
            <p>
              Barangay Pitogo was formerly led by the following Barangay
              Chairmen, Martiniano L. Bao (1956-1960), Francisco L.C. Oslas
